# slicing up the strings

user = "Jai Gupta"
# accessing the string characters individually
# starts from 0
print(user[0])
print(user[3])
# accessing the last character
print(user[-1])

# ranging with the string characters
print(user[2:7])

# if the starting position is not given
print(user[:7])
print(user[:])

# to find the length of the string
print(len(user))
